# Слово 'class' говорит компу: "Открой папку для чертежей, сейчас будем писать правила для сущности 'Human'"
class Human:

    # Это функция РОЖДЕНИЯ (инициализации). Срабатывает 1 раз, когда робот сходит с конвейера.
    # self - это будущий робот. name и age - это данные, которые мы дадим ему при рождении.
    def __init__(self, name, age , gender):
        
        # Компьютер, возьми бумажку с ИМЕНЕМ (которую мы передали в скобках) 
        # и положи её в личный внутренний карман ЭТОГО робота (self.name)
        self.name = name
        
        # Возьми ЧИСЛО возраста и положи во второй карман ЭТОГО робота (self.age)
        self.age = age
        self.gender = gender
        # Просто выведем текст, чтобы видеть момент, когда завод собрал робота
        print(f"[ЗАВОД]: Бип-боп! Создан новый человек с именем {self.name}")


    # =================================================================
    # КНОПКИ ДЕЙСТВИЙ РОБОТА (Методы)
    # =================================================================

    # Кнопка №1: Поздороваться. (self обязательно нужен, чтобы робот знал, кто он такой)
    def say_hello(self):
        # Робот лезет в СВОЙ карман (self.name), достает имя и говорит его
        print(f"Привет! Меня зовут {self.name}, и мне {self.age} лет.")


    # Кнопка №2: Отпраздновать день рождения (изменяет данные в памяти!)
    def celebrate_birthday(self):
        # Робот берет свой текущий возраст (self.age), прибавляет 1 и кладет обратно в карман
        self.age = self.age + 1
        print(f"Ура! {self.name} празднует днюху! Теперь возраст: {self.age}")


human2 = Human("Emirhan" , gender="Male" , age=12)
