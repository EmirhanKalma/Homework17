# ЧАСТЬ 2: РЕАЛЬНЫЙ МИР И ОПЕРАТИВНАЯ ПАМЯТЬ (Тут начинается игра)
# =====================================================================

print("\n--- ИГРА НАЧАЛАСЬ ---")

# 1. Мы просим завод (Human) создать робота. Передаем данные: "Emirhan" и 13.
# Завод штампует робота, кладет его в ОПЕРАТИВНУЮ ПАМЯТЬ.
# А поводок от этого робота мы берем в руку и называем 'player1'.
player1 = Human("Emirhan", 13 , "Male")

# 2. Просим завод создать второго робота.
# Он занимает СОВЕРШЕННО ДРУГОЕ место в памяти. Его поводок - 'player2'.
player2 = Human("Anna", 20 , "Male")

print("\n--- ДЕЙСТВИЯ РОБОТОВ ---")

# Мы дергаем за поводок 'player1', находим Эмирхана в памяти 
# и через точку (.) нажимаем на его спине кнопку say_hello()
player1.say_hello()

# Дергаем за поводок 'player2' и нажимаем ту же кнопку у Анны
player2.say_hello()

print("\n--- ИЗМЕНЕНИЕ ПАМЯТИ ---")

# Мы хотим залезть в карман к player1 и просто посмотреть, что там лежит
print(f"До дня рождения возраст Эмирхана: {player1.age}")

# Нажимаем кнопку празднования. Внутри оперативной памяти цифра 13 прямо сейчас превратится в 14!
player1.celebrate_birthday()

# Снова лезем в карман к Эмирхану, чтобы убедиться, что память обновилась
print(f"После дня рождения возраст Эмирхана: {player1.age}")

# А теперь проверим Анну. Изменился ли её возраст?
# НЕТ! Потому что Anna (player2) - это другой объект в другой ячейке памяти.
print(f"А возраст Анны не изменился, ей всё еще: {player2.age}")









# Return & Print

def make_burger_print():
    burger = "Бургер с сыром"
    print(burger) # Повар кричит на весь зал и выкидывает бургер

# Ты как официант пытаешься забрать бургер на поднос (в переменную 'my_food')
my_food = make_burger_print() 

# Смотрим, что на подносе:
print("У меня на подносе:", my_food)


def make_burger_return():
    burger = "Бургер с сыром"
    return burger # Повар молча отдает бургер тебе в руки

# Забираем бургер на поднос (в переменную 'my_food')
my_food = make_burger_return() 

# Смотрим, что на подносе:
print("У меня на подносе:", my_food)





