# Почему именно так (и почему в скобках)? Вот три главных правила срезов:
# 1. Правая граница НЕ включается
# Когда ты пишешь [старт : стоп], Python берет элементы начиная с индекса старт и идет ДО индекса стоп, но сам индекс стоп не берет.
# Индекс 1 — это цифра 2.
# Индекс 2 — это цифра 3.
# Срез [1:2] говорит: "Возьми элементы начиная с 1-го индекса и остановись перед 2-м". В итоге туда попадает только один элемент — двойка.
# 2. Срез ВСЕГДА возвращает новый список
# Поскольку ты использовал двоеточие :, Python понимает, что ты просишь "отрезать кусок списка". А кусок списка — это тоже список. Поэтому он возвращает двойку внутри квадратных скобок [2], а не просто число 2.
