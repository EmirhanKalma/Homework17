# О, а вот это уже продвинутая магия Python — срезы с шагом! (Кстати, в конце должна быть квадратная скобка ], а не круглая ) 😉)
# Этот код выведет:
# code
# Python
# download
# content_copy
# expand_less
# [1]
# Давай разберем, почему так вышло. Полная формула среза в Python состоит из трех частей:
# [старт : стоп : шаг]
# В твоем случае [0::-1]:
# старт = 0 — начинаем с нулевого индекса (это цифра 1).
# стоп = (пусто) — значит, идем до самого упора.
# шаг = -1 — минус означает, что мы идем в обратном порядке (справа налево).
# Как рассуждает Python:
# Он встает на индекс 0 (берет цифру 1), а затем пытается сделать шаг назад (влево), потому что шаг отрицательный. Но левее нулевого индекса ничего нет! Список там заканчивается. Поэтому он берет только то, что успел, и возвращает [1].
